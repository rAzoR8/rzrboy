			byte[] data = Tiles.From4Bit(
				03333300,
				22000220,
				11000110,
				22222220,
				33000330,
				22000220,
				11000110,
				00000000);

			// replace background tile
			data.CopyTo( TileData, 0 );

			// turn off audio
			ushort Entry = Xor( B ); // A = 0
			Ldh( 0x26, A ); // 0xFF26 rAUDENA 

			ushort WaitVBlank = Ldh( A, 0x44 );
			Cp( 140 );
			Jp( isC, WaitVBlank );

			// turn off LCD
			Xor( A );
			Ldh( 0x40, A ); // rLCDC LCD control

